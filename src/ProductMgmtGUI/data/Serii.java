package ProductMgmtGUI.data;

public class Serii {

    private int idSerii;
    private String numarSerie;
    private int idProdus;

    public int getIdSerii() {
        return idSerii;
    }

    public void setIdSerii(int idSerii) {
        this.idSerii = idSerii;
    }

    public String getNumarSerie() {
        return numarSerie;
    }

    public void setNumarSerie(String numarSerie) {
        this.numarSerie = numarSerie;
    }

    public int getIdProdus() {
        return idProdus;
    }

    public void setIdProdus(int idProdus) {
        this.idProdus = idProdus;
    }
}

